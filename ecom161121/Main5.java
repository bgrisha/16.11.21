//5.	Write a Java program to print reverse an array.
public class Main5 {
	public static void main(String[] args) {
		int array[]= {1,2,4,7};
		
		
		  for (int i = array.length-1; i >= 0; i--) {  
	            System.out.print(array[i] + " ");  
	        }  
		

	}

}